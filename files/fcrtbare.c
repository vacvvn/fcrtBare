#define Q_LEN	    4
#define MSG_MAX_LEN 8
static FCRT_RX_DESC * rx_dsc=NULL;
static u32 rx_cnt;

FCRT_TX_DESC * tx_dsc=NULL;
static u32 tx_cnt;

static u8 ** tx_q=NULL;
static u32 * tx_m_len;
static u32 tx_m_cnt, tx_m_w_ind, tx_m_r_ind;
static u8 ** rx_q=NULL;
static u32 * rx_m_len;
static u32 rx_m_cnt, rx_m_w_ind, rx_m_r_ind;

