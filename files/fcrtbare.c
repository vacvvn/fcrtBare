int fcrtInit(FCRT_INIT_PARAMS * param)
{
	u32 i =0;
    dev_err(dev, "[%s]", __func__);
    fcrt_alloc = param->fcrt_alloc;
	fcrt_free = param->fcrt_free;
    tx_cnt = param->nTx;
    rx_cnt = param->nRx;
	dev = param->dev;
	dma_addr_t no_use;
    // tx_dsc = kzalloc(tx_cnt * sizeof(FCRT_TX_DESC), GFP_KERNEL);
    // rx_dsc = kzalloc(rx_cnt * sizeof(FCRT_RX_DESC), GFP_KERNEL);
    if (tx_q == NULL)
    {
		tx_m_cnt = 0;
		tx_m_w_ind = 0;
		tx_m_r_ind = 0;
#if 1
		dev_err(dev,"\ntx_q alloc");
#endif
		tx_q = (u8**)fcrt_alloc(sizeof(u8*), Q_LEN * sizeof(u8*), &no_use);
		if(tx_q == NULL)
		{
			dev_err(dev,"[%s]cant alloc mem for tx_q", __func__);
			return -ENOMEM;
		}
		tx_m_len = (u32*)fcrt_alloc(sizeof(u32), Q_LEN * sizeof(u32), &no_use);
		if(tx_m_len == NULL)
		{
			dev_err(dev,"[%s]cant alloc mem for tx_m_len buf", __func__);
			return -ENOMEM;
		}
#if 1
		dev_err(dev,"\ntx_q filling");
#endif
		for(i = 0; i < Q_LEN; i++)
		{
			tx_q[i] = fcrt_alloc(sizeof(u8), MSG_MAX_LEN, &no_use);
			if(tx_q[i] == NULL)
			{
				dev_err(dev,"[%s]cant alloc mem for tx_q[%d]", __func__, i);
				return -ENOMEM;
			}
			tx_m_len[i] = 0;
		}
#if 1
		dev_err(dev,"\ntx_q show");
		for(i = 0; i < Q_LEN; i++)
		{
			dev_err(dev,"tx_q[%d]: %p", i, tx_q[i]);
		}
#endif
    }
	
    if (rx_q == NULL)
    {
		rx_m_cnt = 0;
		rx_m_w_ind = 0;
		rx_m_r_ind = 0;
#if 1
		dev_err(dev,"\nrx_q alloc");
#endif
		rx_q = (u8**)fcrt_alloc(sizeof(u8*), Q_LEN * sizeof(u8*), &no_use);
		if(rx_q == NULL)
		{
			dev_err(dev,"[%s]cant alloc mem for rx_q", __func__);
			return -ENOMEM;
		}
		rx_m_len = (u32*)fcrt_alloc(sizeof(u32), Q_LEN * sizeof(u32), &no_use);
		if(rx_m_len == NULL)
		{
			dev_err(dev,"[%s]cant alloc mem for tx_m_len buf", __func__);
			return -ENOMEM;
		}
#if 1
		dev_err(dev,"\nrx_q filling");
#endif
		for(i = 0; i < Q_LEN; i++)
		{
			rx_q[i] = fcrt_alloc(sizeof(u8), MSG_MAX_LEN, &no_use);
			if(rx_q[i] == NULL)
			{
				dev_err(dev,"[%s]cant alloc mem for rx_q[%d]", __func__, i);
				return -ENOMEM;
			}
			rx_m_len[i] = 0;
		}
#if 1
		dev_err(dev,"\nrx_q show");
		for(i = 0; i < Q_LEN; i++)
		{
			dev_err(dev,"rx_q[%d]: %p", i, rx_q[i]);
		}
#endif
    }
    return 0;
}
EXPORT_SYMBOL(fcrtInit);


int fcrtSend(unsigned int vc, void* buf, unsigned int size)
{
	dev_err(dev, "[%s]", __func__);
	if(tx_m_cnt == Q_LEN)
	{
		dev_err(dev,"[%s]tx_msg_queue is full", __func__);
		return -EAGAIN;
	}
	if(size > MSG_MAX_LEN)
	{
		dev_err(dev,"[%s]Max msg len is: %d", __func__, MSG_MAX_LEN);
		return -EINVAL;
	}
	if(copy_from_user(tx_q[tx_m_w_ind], buf, size)!=0)
	{
		dev_err(dev, "[%s]copy from user fails", __func__);
		return -EAGAIN;
	}
	tx_m_len[tx_m_w_ind] = size;
	tx_m_w_ind++;
	if(tx_m_w_ind == Q_LEN)
	{
		tx_m_w_ind = 0;
	}
	tx_m_cnt++;
#if 0
	print_hex_dump(KERN_ALERT, "fcrtSend: ", DUMP_PREFIX_ADDRESS, 32, 1, rx_q, size, true);
#endif

#if 1
	loop_msg();
#endif

	return 0;
}
EXPORT_SYMBOL(fcrtSend);

int fcrtChk(unsigned int vc)
{
	if(rx_m_len > 0)
		return 0;
	else
		return -EAGAIN;
}
EXPORT_SYMBOL(fcrtChk);

int fcrtRecv(unsigned int vc, void* buf, unsigned int * size)
{
	dev_err(dev, "[%s]", __func__);
	if(rx_m_cnt == 0)
	{
		dev_err(dev,"[%s]rcv queue is empty", __func__);
		return -EAGAIN;
	}
	if(copy_to_user(buf, rx_q[rx_m_r_ind], rx_m_len[rx_m_r_ind]) != 0)
	{
		dev_err(dev, "[%s]copy to user fails", __func__);
		return -EAGAIN;
	}
	rx_m_cnt--;
    *size = rx_m_len[rx_m_r_ind];

	rx_m_r_ind++;
	if(rx_m_r_ind == Q_LEN)
	{
		rx_m_r_ind = 0;
	}
#if 1
    print_hex_dump(KERN_ALERT, "fcrtRecv: ", DUMP_PREFIX_ADDRESS, 32, 1, buf, *size,
                   true);
#endif
	return 0;
}
EXPORT_SYMBOL(fcrtRecv);


static void fcrtRelease(void)
{
	dev_err(dev,"[%s]", __func__);
	if(tx_q != NULL)
	{
		fcrt_free(tx_q);
		tx_q = NULL;
	}
	if(rx_q != NULL)
	{
		fcrt_free(rx_q);
		rx_q = NULL;
	}
	if(rx_dsc != NULL)
	{
		fcrt_free(rx_dsc);
		rx_dsc = NULL;
	}
	if(tx_dsc != NULL)
	{
		fcrt_free(tx_dsc);
		tx_dsc = NULL;
	}
}
///копируем сообщение из очереди передачи в очередь приема
static int loop_msg(void)
{
	if(tx_m_cnt == 0)
	{
		dev_err(dev,"[%s]tx msg queue is empty", __func__);
		return -EAGAIN;
	}
	tx_m_cnt--;
	if(rx_m_cnt < Q_LEN)
	{
		rx_m_cnt++;
	}
	else
	{
		dev_warn(dev, "[%s]RcvQueue is full. Overwrite most old received message", __func__);
	}

	memcpy(rx_q[rx_m_w_ind], tx_q[tx_m_r_ind], tx_m_len[tx_m_r_ind]);
	rx_m_len[rx_m_w_ind] = tx_m_len[tx_m_r_ind];
	rx_m_w_ind++;
	if(rx_m_w_ind == Q_LEN)
		rx_m_w_ind = 0;

	tx_m_r_ind++;
	if(tx_m_r_ind == Q_LEN)
		tx_m_r_ind = 0;
	
	return 0;
}
